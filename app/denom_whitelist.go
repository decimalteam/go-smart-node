func init() {
	// Build a regex that matches the standard denom pattern OR any of the whitelisted denoms.
	// These denoms were created via EVM and have active delegations but don't conform to the
	// Cosmos SDK denom regex.
	escaped := make([]string, len(invalidDelegatedDenoms))
	for i, d := range invalidDelegatedDenoms {
		escaped[i] = regexp.QuoteMeta(d)
	}
	whitelist := strings.Join(escaped, "|")

	sdk.SetCoinDenomRegex(func() string {
		return `[a-zA-Z][a-zA-Z0-9/:._-]{2,127}|` + whitelist
	})
}

